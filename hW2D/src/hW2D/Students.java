package hW2D;

public class Students {
	int id;
	String firstName;
	String lastName;
	String emailAddress;
	String password;

	public Students() {

	}

	public Students(int id, String firstName,String lastName, String emailAddress, String password) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.emailAddress = emailAddress;
		this.password = password;
	}

}
